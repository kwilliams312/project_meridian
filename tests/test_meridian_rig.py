# AABB centre). Blender imports Y-up→Z-up, so the sculpt arrives with the blade on
# Z; the restyle proves the pose is canonical regardless of the raw axis order.


def test_restyle_weapon_canonical_pose_matches_the_pickaxe_convention():
    """Blade→Z (up out of the fist), crossguard→X, flat→Y — the pickaxe's pose."""
    assert restyle_weapon.UP_AXIS == 2  # Blender Z → glTF +Y under export_yup
    assert restyle_weapon.GUARD_AXIS == 0  # crossguard along X
    assert restyle_weapon.FLAT_AXIS == 1  # blade flat along Y
    assert {
        restyle_weapon.UP_AXIS,
        restyle_weapon.GUARD_AXIS,
        restyle_weapon.FLAT_AXIS,
    } == {
        0,
        1,
        2,
    }


def test_restyle_weapon_longest_axis_is_the_blade():
    """The blade is the longest AABB extent; guard next; flat shortest."""
    # Raw sword extents (X guard, Y blade, Z flat).
    axes = restyle_weapon.sorted_axes_by_extent((0.73, 2.0, 0.165))
    assert axes == (1, 0, 2)  # blade=Y, guard=X, flat=Z
    # Ties break deterministically by axis index.
    assert restyle_weapon.sorted_axes_by_extent((1.0, 1.0, 1.0)) == (0, 1, 2)


def test_restyle_weapon_grip_side_from_centroid():
    """The guard/grip/pommel mass puts the centroid toward the grip end."""
    # Grip mass on −blade side → centroid below centre → not on the positive side.
    assert (
        restyle_weapon.grip_on_positive_side(
            centroid=(0.0, -0.4, 0.0), aabb_center=(0.0, 0.0, 0.0), blade_axis=1
        )
        is False
    )
    # Mirror case: grip mass on +blade side.
    assert (
        restyle_weapon.grip_on_positive_side(
            centroid=(0.0, 0.4, 0.0), aabb_center=(0.0, 0.0, 0.0), blade_axis=1
        )
        is True
    )


def _apply_perm(perm, p):
    """Reference application of a signed permutation: new[t] = sign * old[src]."""
    out = [0.0, 0.0, 0.0]
    for target in range(3):
        source, sign = perm[target]
        out[target] = sign * p[source]
    return tuple(out)


def test_restyle_weapon_permutation_lands_the_blade_up_and_grip_down():
    """The raw sword pose maps to blade→+Z with the grip seated on −Z."""
    axes = restyle_weapon.sorted_axes_by_extent((0.73, 2.0, 0.165))  # (1,0,2)
    perm = restyle_weapon.signed_permutation(axes, grip_positive=False)
    # Blade tip (raw +Y) lands on +Z (up); the crossguard (raw ±X) lands on X.
    tip = _apply_perm(perm, (0.0, 1.0, 0.0))
    assert tip[restyle_weapon.UP_AXIS] > 0.9
    guard = _apply_perm(perm, (0.365, 0.0, 0.0))
    assert abs(guard[restyle_weapon.GUARD_AXIS]) > 0.3
    # Grip end (raw −Y) lands on −Z (down, into the fist).
    grip = _apply_perm(perm, (0.0, -1.0, 0.0))
    assert grip[restyle_weapon.UP_AXIS] < -0.9


def test_restyle_weapon_permutation_flips_grip_when_on_positive_side():
    """When the grip mass is on the +blade side, the blade is flipped so grip→−Z."""
    axes = (1, 0, 2)
    perm = restyle_weapon.signed_permutation(axes, grip_positive=True)
    # Raw +Y is now the GRIP → it must land DOWN (−Z).
    grip = _apply_perm(perm, (0.0, 1.0, 0.0))
    assert grip[restyle_weapon.UP_AXIS] < -0.9


def test_restyle_weapon_permutation_is_a_proper_rotation_not_a_mirror():
    """Every permutation is a proper rotation (det +1) — never mirrors the mesh."""
    for extents in [(0.73, 2.0, 0.165), (2.0, 0.5, 0.4), (0.3, 0.4, 1.7)]:
        axes = restyle_weapon.sorted_axes_by_extent(extents)
        for grip_positive in (True, False):
            perm = restyle_weapon.signed_permutation(axes, grip_positive)
            rows = {t: perm[t] for t in range(3)}
            assert restyle_weapon._perm_determinant(rows) == 1


