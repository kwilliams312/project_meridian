def _is_lfs_pointer(path: Path) -> bool:
    """True when `path` is an unsmudged Git-LFS pointer rather than a real .glb.

    CI checks out without `lfs: true`, so the binary is a small text pointer
    there; the structural tests skip in that case and run locally (and on any
    LFS-smudged checkout) against the real geometry.
    """
    try:
        with open(path, "rb") as fh:
            return fh.read(64).startswith(b"version https://git-lfs")
    except FileNotFoundError:
        return False


