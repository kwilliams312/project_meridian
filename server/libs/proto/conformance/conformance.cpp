// meridian-proto conformance harness — golden IF-1/IF-2 wire message corpus +
// protocol drift guard (Issue #68).
//
// GOAL: lock the client<->server WIRE PROTOCOL against accidental drift. A
// codec/schema change that alters the FlatBuffers wire encoding of any IF-1
// (auth.fbs) or IF-2 (world.fbs) message must FAIL this test — the network
// analogue of the mcc content-determinism golden (#122), which guards the
// CONTENT pipeline. Contract: client SAD §5.1 ("golden-message round-trip tests
// against the shared /schema fixtures used by the server track"); schema/net
// README ("golden round-trip fixtures live alongside the schemas").
//
// WHAT A GOLDEN IS
// ----------------
// For each message table, a canonical instance is built with the REAL codec
// (meridian::proto's flatc-generated Create*), Finish()ed, and the resulting
// FlatBuffer ROOT-TABLE bytes are frozen to conformance/golden/<name>.bin.
// Those bytes are exactly what login_session.cpp / world_dispatch.cpp put on the
// wire, minus the transport-added framing (u32 LE length for IF-1; u16 opcode +
// u64 seq for IF-2). The framing is a fixed, hand-written codec tested elsewhere
// (net/, worldd-dispatch); the DRIFT-PRONE surface this guard protects is the
// flatc-generated table encoding, so that is what we freeze.
//
// THREE CHECKS PER MESSAGE (any failing fails the gate). All three are
// FLATC-VERSION-INDEPENDENT: none compares freshly-encoded bytes to the golden
// bytes across environments, so the gate never depends on which flatc built it.
//   (1) DECODE-ASSERT   — decode the checked-in golden .bin via the real codec
//       (GetRoot<T> + Verifier) and assert every logical field equals the
//       expected value baked into build_corpus(). This is THE protocol-drift
//       guard: it catches a schema change that shifts field meaning / vtable
//       layout. It is version-STABLE because the FlatBuffers WIRE FORMAT is
//       forward/backward compatible — bytes written by any flatc decode to the
//       same fields under any other. The golden .bin is never regenerated by a
//       version bump; it is regenerated only when the protocol intentionally
//       changes.
//   (2) SAME-BUILD DETERMINISM — encode the canonical message twice with the
//       SAME code + SAME flatc (this build) and assert the two encodings are
//       byte-identical. Proves our encoder is deterministic WITHIN a build
//       (fixed field-population order, no maps/hashing) without ever comparing
//       to the golden. A nondeterministic encoding surfaces as a self-diff.
//   (3) SEMANTIC ROUND-TRIP — re-encode the canonical message with this build,
//       decode THAT, and assert its logical fields match the canonical
//       expectations (the same expectations the golden is asserted against in
//       (1)). This proves encode∘decode = identity version-stably; combined with
//       (1) it establishes that a fresh encode is SEMANTICALLY equal to the
//       golden without requiring byte-identity.
//
// WHY NOT BYTE-IDENTITY VS THE GOLDEN?
// FlatBuffers does NOT guarantee byte-identical encoder output across flatc
// versions: vtable dedup, alignment/padding, and vector layout differ between
// releases (a vector-containing table like RealmList drifts even when every
// field is unchanged). The golden here is generated with macOS Homebrew flatc,
// while CI builds with the apt flatbuffers-compiler (a different version), so a
// fresh-bytes-vs-golden memcmp fails on CI for a NON-BUG. Byte-identity across
// environments is the wrong invariant; the wire format's decode stability (1) +
// same-build determinism (2) + semantic round-trip (3) are the right ones
// (regression from #259, fixed in #264).
//
// MODES
//   (default / --check)  run the gate: for every message, decode-assert the
//                        golden, check same-build re-encode determinism, and
//                        semantic round-trip. Non-zero exit on any mismatch, with
//                        an actionable message.
//   --update             regenerate conformance/golden/*.bin + manifest.json from
//                        the current codec, then exit 0. Review the golden diff
//                        exactly as you would review the schema/codec diff — a
//                        golden change IS a wire-protocol change — and commit both
//                        together.
//
// Registered in ctest as `proto-conformance` (server/libs/proto/CMakeLists.txt);
// the CI `server` job runs `ctest --test-dir server/build`, so wire drift fails CI.

